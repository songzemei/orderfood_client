package domain;

public class MyException extends Exception{
    private String message;
    private String code;

    public MyException(String message, String code) {
        super(message);
        this.message = message;
        this.code = code;
    }

    @Override
    public String getMessage() {
        return message;
    }

    public String getCode() {
        return code;
    }
}
